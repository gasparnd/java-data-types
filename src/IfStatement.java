public class IfStatement {
    public static void main(String[] args) {
        boolean isBluetoothEnabled = false;

        if (isBluetoothEnabled) {
            System.out.println("Yes");
        } else if (!isBluetoothEnabled) {
            System.out.println("Nop");
        } else {
            System.out.println(":/");
        }
    }
}
